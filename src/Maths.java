public class Maths {
    public int mult(int x, int y){
        int result = x*y;
        return result;
    }
    public int add(int x, int y){
        int val = x +y;
        return val;
    }

    public float div(int x, int y){
        int res = x/y;
        return res;
    }

    public boolean isEven(int x){
        return x %2 == 0;
    }

}
